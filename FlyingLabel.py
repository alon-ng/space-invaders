from time import sleep


class FlyingLabel:

    def __init__(self, str, stdscr):
        self.stdscr = stdscr
        self.strings = str.split('\n')

    def start(self):
        h, w = self.stdscr.getmaxyx()

        # Preforms the transition into the screen
        for length in range(len(self.strings[0]) + 1):
            self.stdscr.clear()
            for i, row in enumerate(self.strings):
                self.stdscr.addstr(i + 10, 0, row[len(row) - length:])
            self.stdscr.refresh()
            sleep(0.01)

        # Preforms the transition on the screen
        x = 0
        while (x < w - len(self.strings[0])):
            self.stdscr.clear()
            for i, row in enumerate(self.strings):
                self.stdscr.addstr(i + 10, x, row)
            self.stdscr.refresh()
            sleep(0.01)
            x += 1

        # Preforms the transition out of the screen
        for length in range(len(self.strings[0]) - 1):
            self.stdscr.clear()
            for i, row in enumerate(self.strings):
                t = w - len(row[:len(row) - length])
                self.stdscr.addstr(
                    i + 10, w - len(row[:len(row) - length]), row[:len(row) - length])
            self.stdscr.refresh()
            sleep(0.01)


